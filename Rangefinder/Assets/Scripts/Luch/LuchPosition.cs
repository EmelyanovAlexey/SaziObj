using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class LuchPosition
{
    public float x;
    public float y;
    public float z;
    public int r;
    public int g;
    public int b;

    public LuchPosition(float x, float y, float z, int r, int g, int b)
    {
        this.x = x;
        this.y = y;
        this.z = z;
        this.r = r;
        this.g = g;
        this.b = b;
    }
}
